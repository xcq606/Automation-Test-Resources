"""
Python 解释器可以作为一个简单的计算器，您可以在解释器里输入一个表达式，它将输出表达式的值。
表达式的语法很直白： +, -, * 和 /, 和其它语言（如Pascal或C）里一样
"""
a, b, c = 11, 22, 33
print("a = ", a)
print("b = ", b)
print("c = ", c)
print("a + b + c = ", a + b + c)
print("a - b = ", a - b)
print("a * b = ", a * b)
print("a / b = ", a / b)
